package com.yys.auto.entity;

/**
 * describe: 坐标对象
 *
 * @author lianying
 * @date 2019/1/5
 */
public class Coordinate {
    int x;
    int y;

    public Coordinate(int x, int y) {
        this.x = x ;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
